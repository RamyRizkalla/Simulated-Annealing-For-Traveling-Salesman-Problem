
public class City 
{
   private double xLoc = 0;
   private double yLoc = 0;
    
   
   
   public double x()
   {
      return xLoc;
   }
        
   public void setXLoc(double xCoordinate)
   {
     this.xLoc = xCoordinate;
   }
    
   public double y()
   {
     return yLoc;
   }
        
   public void setYLoc(double yCoordinate)
   {
      this.yLoc = yCoordinate;
   }
  

}
